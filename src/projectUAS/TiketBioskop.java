    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        final double tiket = 40000;
        double subtotal = 0;
        double pajak = 0;
        double total = 0;

        DecimalFormat formatter = new DecimalFormat("Rp #,##0");

        // Calculate the subtotal based on the selected seats
        int jumlahTiket = 0;
        
        char[] seatLetters = {'A', 'B', 'C'};
        int[] seatNumbers = {1, 2, 3, 4, 5, 6, 7};

       Function<String, JCheckBox> getCheckBox = seat -> {
        switch (seat) {
            case "A1":
                return chkBoxA1;
            case "A2":
                return chkBoxA2;
            case "A3":
                return chkBoxA3;
            case "A4":
                return chkBoxA4;
            case "A5":
                return chkBoxA5;
            case "A6":
                return chkBoxA6;
            case "A7":
                return chkBoxA7;
            case "B1":
                return chkBoxB1;
            case "B2":
                return chkBoxB2;
            case "B3":
                return chkBoxB3;
            case "B4":
                return chkBoxB4;
            case "B5":
                return chkBoxB5;
            case "B6":
                return chkBoxB6;
            case "B7":
                return chkBoxB7;
            case "C1":
                return chkBoxC1;
            case "C2":
                return chkBoxC2;
            case "C3":
                return chkBoxC3;
            case "C4":
                return chkBoxC4;
            case "C5":
                return chkBoxC5;
            case "C6":
                return chkBoxC6;
            case "C7":
                return chkBoxC7;
            default:
                return null;
        }   
    };

        for (char letter : seatLetters) {
            for (int number : seatNumbers) {
                String seat = String.format("%c%d", letter, number);
                JCheckBox checkBox = getCheckBox.apply(seat);

                if (checkBox != null && checkBox.isSelected()) {
                    subtotal += tiket;
                    jumlahTiket++;
                }
            }
        }

        System.out.println("Subtotal before formatting: " + subtotal); // Add this line to print subtotal
        pajak = 0.02 * subtotal;
        total = pajak + subtotal;
        
        lblbayar.setText(formatter.format(subtotal));
        System.out.println("Subtotal after formatting: " + lblbayar.getText());
       
        lblPajak.setText(formatter.format(pajak));
        lblTotal.setText(formatter.format(total));

        overralltotalsale = overralltotalsale + total;
        this.jumlahTiket = jumlahTiket;

    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnDetailPembelianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailPembelianActionPerformed
        // TODO add your handling code here:
        // Count the selected checkboxes
        jumlahCheckBox = countSelectedCheckboxes();

        // Get the selected checkbox text
        String selectedCheckboxText = getSelectedCheckboxText();

        lblNamaFilm.setText((String)cmbfilm.getSelectedItem());
        lblJumNo.setText(jumlahCheckBox + " | " + selectedCheckboxText);
        lblTgldanWaktu.setText((String)cmbTanggal.getSelectedItem() + " | " + (String)cmbWaktu.getSelectedItem());
        lblTotalBayar.setText(lblTotal.getText());

        purchasesave[0][0] = "Nama Film: " + (String)cmbfilm.getSelectedItem();
        purchasesave[1][0] = "Jumlah Tiket | No. Kursi: " + jumlahCheckBox + " | " + selectedCheckboxText;
        purchasesave[2][0] = "Tanggal dan Waktu: " + (String)cmbTanggal.getSelectedItem() + " | " + (String)cmbWaktu.getSelectedItem();
        purchasesave[3][0] = "Total Bayar: " + lblTotal.getText();

        try {
            Connection conn = KoneksiDB.getKoneksi();

            String sql = "INSERT INTO pembelian (id_pembelian, id_user, film, tanggal, waktu, jumlah_tiket, total_bayar, no_bangku) VALUES (?,?,?,?,?,?,?,?)";

            PreparedStatement p = conn.prepareStatement(sql);

            // Use the method to generate purchase ID
            p.setString(1, generatePurchaseId());
            p.setInt(2, userId);
            p.setString(3, (String)cmbfilm.getSelectedItem());
            p.setString(4, (String)cmbTanggal.getSelectedItem());
            p.setString(5, (String)cmbWaktu.getSelectedItem());
            p.setInt(6, jumlahCheckBox);
            p.setString(7, lblTotal.getText());
            p.setString(8, selectedCheckboxText);

            p.executeUpdate();
            p.close();
        } catch (SQLException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error while inserting data into the database.");
        }
    }//GEN-LAST:event_btnDetailPembelianActionPerformed

