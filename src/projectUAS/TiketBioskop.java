private int countSelectedCheckboxes() {
    int count = 0;

    // Add all your checkboxes to this array
    JCheckBox[] checkboxes = {chkBoxA1, chkBoxA2, chkBoxA3, chkBoxA4, chkBoxA5, chkBoxA6, chkBoxA7,
            chkBoxB1, chkBoxB2, chkBoxB3, chkBoxB4, chkBoxB5, chkBoxB6, chkBoxB7,
            chkBoxC1, chkBoxC2, chkBoxC3, chkBoxC4, chkBoxC5, chkBoxC6, chkBoxC7};

    for (JCheckBox checkbox : checkboxes) {
        if (checkbox.isSelected()) {
            count++;
        }
    }

    return count;
}

// New method to get selected checkbox text
private String getSelectedCheckboxText() {
    StringBuilder selectedText = new StringBuilder();

    // Add all your checkboxes to this array
    JCheckBox[] checkboxes = {chkBoxA1, chkBoxA2, chkBoxA3, chkBoxA4, chkBoxA5, chkBoxA6, chkBoxA7,
            chkBoxB1, chkBoxB2, chkBoxB3, chkBoxB4, chkBoxB5, chkBoxB6, chkBoxB7,
            chkBoxC1, chkBoxC2, chkBoxC3, chkBoxC4, chkBoxC5, chkBoxC6, chkBoxC7};

    for (JCheckBox checkbox : checkboxes) {
        if (checkbox.isSelected()) {
            selectedText.append(checkbox.getText()).append(", ");
        }
    }

    // Remove the trailing comma and space if any
    if (selectedText.length() > 0) {
        selectedText.delete(selectedText.length() - 2, selectedText.length());
    }

    return selectedText.toString();
}


