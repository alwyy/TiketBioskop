// Add this new method to check if seats are already booked
private void checkBookedSeats() {
    // Replace "selectedFilm", "selectedDate", and "selectedTime" with your actual variables
    String selectedFilm = (String) cmbfilm.getSelectedItem();
    String selectedDate = (String) cmbTanggal.getSelectedItem();
    String selectedTime = (String) cmbWaktu.getSelectedItem();
    
    System.out.println("Selected Film: " + selectedFilm);
    System.out.println("Selected Date: " + selectedDate);
    System.out.println("Selected Time: " + selectedTime);

    try {
        Connection conn = KoneksiDB.getKoneksi();
        String sql = "SELECT no_bangku FROM pembelian WHERE film=? AND tanggal=? AND waktu=?";
        PreparedStatement p = conn.prepareStatement(sql);
        p.setString(1, selectedFilm);
        p.setString(2, selectedDate);
        p.setString(3, selectedTime);

        ResultSet rs = p.executeQuery();

        while (rs.next()) {
            String bookedSeat = rs.getString("no_bangku");
            // Disable the checkbox for the booked seat
            disableCheckbox(bookedSeat);
        }
        
        p.close();
        rs.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error saat memasukkan data.");
    }
}

